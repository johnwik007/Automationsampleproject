package flowcontrolstatements;

public class ForLoop {

	public static void main(String[] args) {      //   for (initialisation ;condition ;increment/decrement )              
		for (int i=0;i<5;i++)                     //   {
		 {                                         //        body;    
			System.out.println("puja");            //   }
		 }
		
	//	Example- 1: Inside the for loop initialization part is optional.	
		
		int i=0;
		for (;i<3;i++)
		{
			System.out.println("subol");
		}
		
		
		}
	

	}


